import Dashboard from "./pages/Dashboard"
import { useEffect } from "react";

function App() {
  useEffect(() => {
    const theme = localStorage.getItem('theme');
    if (theme === 'dark') {
      document.documentElement.classList.add('dark');
    } else {
      document.documentElement.classList.remove('dark');
    }
  }, []);

  return (
    <Dashboard />
  )
}

export default App;
